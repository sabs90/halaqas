async function geocode(suburb) {
  const url = `https://nominatim.openstreetmap.org/search?q=${encodeURIComponent(suburb + ', Australia')}&format=json&limit=1`;
  const res = await fetch(url, {
    headers: { 'User-Agent': 'halaqas-suburb-gen/1.0' },
  });
  const data = await res.json();
  if (data.length === 0) return null;
  return {
    latitude: parseFloat(parseFloat(data[0].lat).toFixed(4)),
    longitude: parseFloat(parseFloat(data[0].lon).toFixed(4)),
  };
}

async function main() {
  const results = {};
  let count = 0;

  for (const suburb of SUBURBS) {
    count++;
    process.stdout.write(`[${count}/${SUBURBS.length}] ${suburb}...`);
    const coords = await geocode(suburb);
    if (coords) {
      results[suburb] = coords;
      process.stdout.write(` ${coords.latitude}, ${coords.longitude}\n`);
    } else {
      process.stdout.write(` NOT FOUND\n`);
    }
    // Nominatim rate limit: 1 req/sec
    await new Promise((r) => setTimeout(r, 1100));
  }

  // Generate TypeScript file
  const lines = Object.entries(results)
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([k, v]) => `  '${k}': { latitude: ${v.latitude}, longitude: ${v.longitude} },`);

  const ts = `/**
 * Pre-computed coordinates for ~${lines.length} common Australian suburbs.
 * Generated by: node scripts/generate-suburb-coords.mjs
 *
 * Used for instant suburb-radius search without geocode API calls.
 * Mosque-derived coords take precedence at runtime (more precise).
 */
export const SUBURB_COORDS: Record<string, { latitude: number; longitude: number }> = {
${lines.join('\n')}
};
`;

  const fs = await import('fs');
  fs.writeFileSync('src/lib/suburb-coords.ts', ts);
  console.log(`\nWrote ${lines.length} suburbs to src/lib/suburb-coords.ts`);
}

main().catch(console.error);
